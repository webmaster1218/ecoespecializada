#!/usr/bin/env node
/**
 * Pre-build script: Reads .md files from /content and generates
 * src/lib/blog/posts-data.ts with serialized post data.
 * 
 * Run: node scripts/generate-posts-data.js
 * This should run before next build.
 */

const fs = require('fs');
const path = require('path');

const CONTENT_DIR = path.join(process.cwd(), 'content');
const OUTPUT_FILE = path.join(process.cwd(), 'src/lib/blog/posts-data.ts');

function parseFrontmatter(raw) {
  const data = {};
  const lines = raw.split('\n');
  let i = 0;
  let currentKey = '';
  let inArray = false;
  let arrayValues = [];

  while (i < lines.length) {
    const line = lines[i];

    if (inArray && line.match(/^\s+-\s+/)) {
      arrayValues.push(line.replace(/^\s+-\s+/, '').replace(/^["']|["']$/g, ''));
      i++;
      continue;
    }

    if (inArray) {
      data[currentKey] = arrayValues;
      inArray = false;
      arrayValues = [];
    }

    const kvMatch = line.match(/^(\w+):\s*(.*)/);
    if (kvMatch) {
      currentKey = kvMatch[1];
      const val = kvMatch[2].trim();

      if (val === '') {
        if (i + 1 < lines.length && lines[i + 1].match(/^\s+-\s+/)) {
          inArray = true;
          arrayValues = [];
        } else {
          data[currentKey] = '';
        }
      } else if (val.match(/^\d+$/)) {
        data[currentKey] = parseInt(val, 10);
      } else if (val === 'true' || val === 'false') {
        data[currentKey] = val === 'true';
      } else {
        data[currentKey] = val.replace(/^['"]|['"]$/g, '');
      }
    }
    i++;
  }

  if (inArray) {
    data[currentKey] = arrayValues;
  }

  return data;
}

function parseMarkdownFile(filepath) {
  const raw = fs.readFileSync(filepath, 'utf-8');
  const fmMatch = raw.match(/^---\n([\s\S]*?)\n---\n\n?([\s\S]*)$/);
  if (!fmMatch) {
    throw new Error(`Invalid frontmatter in ${filepath}`);
  }
  return { frontmatter: parseFrontmatter(fmMatch[1]), content: fmMatch[2].trim() };
}

function findMdFiles(dir) {
  const files = [];
  if (!fs.existsSync(dir)) return files;
  const entries = fs.readdirSync(dir, { withFileTypes: true });
  for (const entry of entries) {
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) files.push(...findMdFiles(full));
    else if (entry.name.endsWith('.md')) files.push(full);
  }
  return files;
}

// --- Main ---
const files = findMdFiles(CONTENT_DIR);
const posts = [];

for (const filepath of files) {
  try {
    const { frontmatter, content } = parseMarkdownFile(filepath);
    posts.push({
      articleId: frontmatter.articleId || '',
      slug: frontmatter.slug || '',
      title: frontmatter.title || '',
      excerpt: frontmatter.excerpt || '',
      content,
      author: frontmatter.author || 'Equibiomed',
      date: frontmatter.date || '',
      image: frontmatter.image || '',
      category: frontmatter.category || 'guias',
      readTime: frontmatter.readTime || 5,
      tags: frontmatter.tags || [],
      status: frontmatter.status || 'published',
    });
  } catch (err) {
    console.error(`Error loading ${filepath}:`, err.message);
  }
}

// Sort by date desc
posts.sort((a, b) => new Date(b.date) - new Date(a.date));

// Generate TypeScript file
function escapeForTs(str) {
  return str
    .replace(/\\/g, '\\\\')
    .replace(/`/g, '\\`')
    .replace(/\$/g, '\\$')
    .replace(/{{/g, '\\{\\{');
}

let output = `// AUTO-GENERATED by scripts/generate-posts-data.js\n// DO NOT EDIT — edit .md files in /content instead\n\nimport { BlogPost } from './types';\n\nexport const postsData: BlogPost[] = [\n`;

for (const post of posts) {
  output += `  {\n`;
  output += `    articleId: "${post.articleId}",\n`;
  output += `    slug: "${post.slug}",\n`;
  output += `    title: "${post.title.replace(/"/g, '\\"')}",\n`;
  output += `    excerpt: "${post.excerpt.replace(/"/g, '\\"').replace(/\n/g, ' ')}",\n`;
  output += `    content: \`${escapeForTs(post.content)}\`,\n`;
  output += `    author: "${post.author}",\n`;
  output += `    date: "${post.date}",\n`;
  output += `    image: "${post.image}",\n`;
  output += `    category: "${post.category}",\n`;
  output += `    readTime: ${post.readTime},\n`;
  output += `    tags: [${post.tags.map(t => `"${t}"`).join(', ')}],\n`;
  output += `    status: "${post.status}" as const,\n`;
  output += `  },\n`;
}

output += `];\n`;

fs.writeFileSync(OUTPUT_FILE, output, 'utf-8');
console.log(`✅ Generated ${OUTPUT_FILE} with ${posts.length} posts`);
for (const p of posts) {
  console.log(`   ${p.articleId} | ${p.status} | ${p.slug}`);
}
